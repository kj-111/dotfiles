public class Main {
    public static void main(String[] args) {
        System.out.println(greeting());
    }

    static String greeting() {
        return "Hello from __PROJECT_NAME__";
    }

    static boolean isEven(int number) {
        return number % 2 == 0;
    }

    static int divide(int left, int right) {
        return left / right;
    }
}
